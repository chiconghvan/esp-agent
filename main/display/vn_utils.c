void vn_strip_diacritics(char *dst, const char *src, size_t dst_size)
{
    if (dst == NULL || src == NULL || dst_size == 0) return;

    const uint8_t *s = (const uint8_t *)src;
    size_t d = 0;

    while (*s && d < dst_size - 1) {
        /* ASCII thuбә§n (1 byte) */
        if (*s < 0x80) {
            dst[d++] = (char)*s;
            s++;
            continue;
        }

        /* 3-byte UTF-8? (0xE1 xx xx) */
        if (s[0] == 0xE1 && s[1] && s[2]) {
            bool found = false;
            for (size_t i = 0; i < MAP3_COUNT; i++) {
                if (s[0] == map3[i].b1 && s[1] == map3[i].b2 && s[2] == map3[i].b3) {
                    dst[d++] = map3[i].ascii;
                    s += 3;
                    found = true;
                    break;
                }
            }
            if (found) continue;
            /* Unknown 3-byte: skip */
            s += 3;
            continue;
        }

        /* 2-byte UTF-8? (0xC0..0xDF xx) */
        if (*s >= 0xC0 && *s <= 0xDF && s[1]) {
            bool found = false;
            for (size_t i = 0; i < MAP2_COUNT; i++) {
                if (s[0] == map2[i].b1 && s[1] == map2[i].b2) {
                    dst[d++] = map2[i].ascii;
                    s += 2;
                    found = true;
                    break;
                }
            }
            if (found) continue;
            /* Unknown 2-byte: skip */
            s += 2;
            continue;
        }

        /* 4-byte UTF-8 (emoji etc): skip */
        if (*s >= 0xF0 && *s <= 0xF7) {
            s += 4;
            continue;
        }

        /* Fallback: skip byte */
        s++;
    }

    dst[d] = '\0';
}
